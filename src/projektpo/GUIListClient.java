    private String[] Banki() {
        String[] nazwyBankow = new String[centrum.listaBankow.size() + 1];
        nazwyBankow[0] = "Wybierz BANK";
        for (int i = 0; i < centrum.listaBankow.size(); i++) {
            nazwyBankow[i + 1] = centrum.listaBankow.get(i).getNazwaBanku();
        }
        return nazwyBankow;
    }

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {
        String wybranyBank = jComboBox1.getSelectedItem().toString();
        listaKlientow(wybranyBank);
    }

    private void listaKlientow(String wybranyBank) {
        listaWszystkichKlientow.clear();
        if (!wybranyBank.isEmpty()) {
            for (Banki b : centrum.listaBankow) {
                if (b.getNazwaBanku().equals(wybranyBank)) {
                    listaWszystkichKlientow.addAll(b.bazaDanych.listaKlientow);
                    break;
                }
            }
        }
        jList1.setModel(new javax.swing.AbstractListModel<String>() {
            public int getSize() {
                return listaWszystkichKlientow.size();
            }

            public String getElementAt(int i) {
                Klient k = listaWszystkichKlientow.get(i);
                Banki b = centrum.znajdzBankPoPeseluOrazIDKlienta(k.getPESEL(), k.getID());
                return k.toString() + " Bank: " + b.getNazwaBanku();
            }
        });

        jList1.updateUI();
    }

